    private void ADD_HOTELActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADD_HOTELActionPerformed
        if (jComboBox3.getSelectedIndex() != 0 && jComboBox4.getSelectedIndex() != 0) {
            String city = jComboBox3.getSelectedItem().toString();
            String hotelname = HotelTF.getText();
            String stars = jComboBox4.getSelectedItem().toString();
            int starscount = Integer.parseInt(stars.substring(0, 1));
            for (stars = ""; starscount > 0; starscount--) {
                stars = stars + "*";
            }
            double cost = Double.parseDouble(CostTF.getText());
            try {
                Connection conn = SQLConnection.connect();
                Statement stmt = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                String query = "insert into hotel values('" + city + "','" + hotelname + "','" + stars + "'," + cost;
                int count = stmt.executeUpdate(query);
                if (count > 0) {
                    JOptionPane.showMessageDialog(null, "Data Successfully added");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "SELECT A CITY FIRST!!!");
        }
    }//GEN-LAST:event_ADD_HOTELActionPerformed

