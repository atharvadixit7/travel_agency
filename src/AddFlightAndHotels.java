    private void ADD_FLIGHTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADD_FLIGHTActionPerformed
        if (jComboBox1.getSelectedIndex() != 0 && jComboBox2.getSelectedIndex() != 0) {
            String company = jComboBox1.getSelectedItem().toString();
            String bplace = jTextField1.getText();
            String dplace = jTextField2.getText();
            long flightno = Long.parseLong(jTextField3.getText());
            double eper = Double.parseDouble(jTextField4.getText());
            double dper = Double.parseDouble(jTextField5.getText());
            try {
                Connection conn = SQLConnection.connect();
                Statement stmt = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                String query = "insert into flights values('" + company + "','" + bplace + "','" + dplace + "'," + flightno + ",";
                int count = stmt.executeUpdate(query);
                if (count > 0) {
                    JOptionPane.showMessageDialog(null, "Data Successfully added");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "SELECT A PACKAGE AND COMPANY FIRST!!!");
        }
}//GEN-LAST:event_ADD_FLIGHTActionPerformed

